export const getUseCaseById = (id: string): UseCase | undefined => {
  return useCases.find(uc => uc.id === id);
};

export const getUseCasesByCategory = (category: UseCaseCategory): UseCase[] => {
  return useCases.filter(uc => uc.category === category);
};

export const getUseCasesForTier = (tier: 'solo' | 'pro' | 'enterprise'): UseCase[] => {
  return useCases.filter(uc => uc.tier.includes(tier));
};

export const getUseCasesForIndustry = (industry: string): UseCase[] => {
  return useCases.filter(uc => uc.industry.includes(industry));
};

export const getAllCategories = (): { id: UseCaseCategory; label: string }[] => {
  const categories = [...new Set(useCases.map(uc => uc.category))];
  return categories.map(id => ({ id, label: categoryLabels[id] }));
};
